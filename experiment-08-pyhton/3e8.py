#3.	Design a GUI for student registration for a course and store these details in a database. Use Tkinter for UI, SQLite/MySQL for database storage.
import tkinter as tk
import sqlite3
def register():
    name=entry_name.get()
    age=entry_age.get()
    course=entry_course.get()
    conn=sqlite3.connect('students.db')
    c=conn.cursor()
    c.execute('''CREATE TABLE IF NOT EXISTS students (name TEXT, age INTEGER, course TEXT)''')
    c.execute("INSERT INTO students VALUES (?, ?, ?)", (name, age, course))
    conn.commit()
    conn.close()
    label_result.config(text="Student Registered Successfully")
root=tk.Tk()
root.title("Student Registration")
root.geometry("300x200")
label_name=tk.Label(root,text="Name:")
label_name.pack()
entry_name=tk.Entry(root)
entry_name.pack()
label_age=tk.Label(root,text="Age:")
label_age.pack()
entry_age=tk.Entry(root)
entry_age.pack()
label_course=tk.Label(root,text="Course:")
label_course.pack()
entry_course=tk.Entry(root)
entry_course.pack()
button_register=tk.Button(root,text="Register",command=register)
button_register.pack()
label_result=tk.Label(root,text="")
label_result.pack()
root.mainloop()

